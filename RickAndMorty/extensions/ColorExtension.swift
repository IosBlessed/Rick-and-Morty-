import Foundation
import UIKit
extension UIView{
    func setBackgroundColor()->UIView{
        let testView = UIView()
        testView.backgroundColor = UIColor(displayP3Red: 229.0, green: 229.0, blue: 234.0, alpha: 0.85)
        return testView
    }
}
